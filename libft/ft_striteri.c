#include "libft.h"

void	ft_striteri(char *s, void (*f)(uint32_t, char *))
{
	int i;

	i = 0;
	if (s != NULL && f != NULL)
		while (s[i])
		{
			f(i, &s[i]);
			i++;
		}
}
